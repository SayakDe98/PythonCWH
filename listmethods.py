l1 = [1 , 8 , 7 , 2 , 21 , 15]

l1.reverse()    #reverses the list
print(l1)

l1.sort()   #sorts original list since lists are mutable
print(l1)

l1.append(45)   #adds 45 to end of list
l1.append(81)   #adds 81 to end of list
print(l1)

l1.insert(1,544)  #adds 544 at index 1 
print(l1)

l1.pop(2)   #removes element 2 from list
print(l1)

